package com.pm.entity;

public class SharePicture {
    private int sharePictureId;

    private String sharePictureName;

    private String sharePictureAttribute;

    private String sharePictureImg;
    private int shareId;



    public int getSharePictureId() {
        return sharePictureId;
    }

    public void setSharePictureId(int sharePictureId) {
        this.sharePictureId = sharePictureId;
    }

    public String getSharePictureName() {
        return sharePictureName;
    }

    public void setSharePictureName(String sharePictureName) {
        this.sharePictureName = sharePictureName;
    }

    public String getSharePictureAttribute() {
        return sharePictureAttribute;
    }

    public void setSharePictureAttribute(String sharePictureAttribute) {
        this.sharePictureAttribute = sharePictureAttribute;
    }

    public String getSharePictureImg() {
        return sharePictureImg;
    }

    public void setSharePictureImg(String sharePictureImg) {
        this.sharePictureImg = sharePictureImg;
    }

    public int getShareId() {
        return shareId;
    }

    public void setShareId(int shareId) {
        this.shareId = shareId;
    }

}
