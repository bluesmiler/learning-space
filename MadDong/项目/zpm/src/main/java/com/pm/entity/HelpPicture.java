package com.pm.entity;

public class HelpPicture {

    private int helpPictureId;

    private String  helpPictureName;

    private String  helpPictureAttribute;

    private int helpId;

    public int getHelpId() {
        return helpId;
    }

    public void setHelpId(int helpId) {
        this.helpId = helpId;
    }

    public int getHelpPictureId() {
        return helpPictureId;
    }

    public void setHelpPictureId(int helpPictureId) {
        this.helpPictureId = helpPictureId;
    }

    public String getHelpPictureName() {
        return helpPictureName;
    }

    public void setHelpPictureName(String helpPictureName) {
        this.helpPictureName = helpPictureName;
    }

    public String getHelpPictureAttribute() {
        return helpPictureAttribute;
    }

    public void setHelpPictureAttribute(String helpPictureAttribute) {
        this.helpPictureAttribute = helpPictureAttribute;
    }


}
